/* 
1. Sum of Two Numbers
Write a program that stores the integers 50 and 100 in variables, and stores the sum of
these two in a variable names total.
*/

#include <iostream>

int main()
{
	using namespace std;

	int a = 50, b = 100;
	int total = a + b;

	cout << "a = " << a << ", b = " << b << endl;
	cout << "a + b = " << total << endl;

	return 0;
}